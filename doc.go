/*
Package sounds generates and manipulates sounds.

Overview

time.Duration based Signals with their y-axis ranging from +1 to -1, and with some methods to help handle music.

Interfaces

Sound:- time limited Signal (a signals.LimitedFunction.)

Tone:- neverending Sine wave, (a signals.PeriodFunction.)

Note:- a Sound with a duration set to a whole number of Period's, (a signals.PeriodicLimitedFunction.)

(along with basic access with whats in this package, these interfaces can be used by func's from the signals package)

Types

Compositor:- an array of signals.Function, potentially Sound's, that is itself a Sound, and is generated by adding together its contents.

(the end time of a Compositor (that makes it a signals.LimitedFunction), comes from the longest contained signals.Function's, so at least one should be a Sound)

*/
package sound
